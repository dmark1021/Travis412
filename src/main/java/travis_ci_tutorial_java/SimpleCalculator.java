package travis_ci_tutorial_java;

public class SimpleCalculator {
	public int add(int a, int b) {
		return a + b;
	}

	  public static int minus (int a, int b)
	   {
		   return a-b;
	   }
	   
	   public static int multiple(int a, int b)
	   {
		   return a*b;
	   }
	   
	   public static int divide(int a, int b)
	   {
		   return a/b;
	   }
	
	
}
